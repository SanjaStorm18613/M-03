package org.firstinspires.ftc.team18613.utils;

public class FloatPair {

    float a;
    float b;

    public FloatPair(float a, float b) {

        this.a = a;
        this.b = b;

    }

    public float firstValue() {
        return a;
    }

    public float secondValue() {
        return b;
    }
}
